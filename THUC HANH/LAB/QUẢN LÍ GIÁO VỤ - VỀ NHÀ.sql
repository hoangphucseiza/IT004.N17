-- Câu 2: Mã học viên là một chuỗi 5 ký tự, 3 ký tự đầu là mã lớp, 2 ký tự cuối cùng là số thứ tự học viên trong lớp. VD: “K1101” (Trigger)
CREATE TRIGGER Trg_Phan1_Cau2
ON HOCVIEN FOR INSERT, UPDATE 
AS
BEGIN
	DECLARE @MAHV char(5), @MALOP char(3), @SISO tinyint
	SELECT @MAHV = MAHV , @MALOP = MALOP FROM INSERTED
	SELECT @SISO = SISO FROM LOP WHERE LOP.MALOP = @MALOP
	IF LEFT(@MAHV,3) <> @MALOP
	BEGIN
		rollback transaction
		print'3 ki tu dau phai la ma lop'
	END
	ELSE IF CAST(RIGHT(@MAHV,2) AS INT) NOT BETWEEN 1 AND @SISO
	BEGIN
		rollback transaction
		print'2 ki tu cuoi phai la so thu tu trong lop'
	END
	ELSE
	BEGIN
		print'Cap nhap thanh cong!!'
	END
END

